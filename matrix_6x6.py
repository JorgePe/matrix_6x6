def get_representation(c):
    """
    returns 6x6 representation of c (if supported)
    or chr_null
    """
    if c in '0123456789':
        return digits[int(c)]
    elif ord(c) in range(ord('A'), ord('Z')+1):
        return letters[ord(c)- 65]
    elif ord(c) in range(ord('a'), ord('z')+1):
        return letters[ord(c)- 97]
    elif c == '?':
        return chr_question
    elif c == '!':
        return chr_exclam
    elif c == '@':
        return chr_smile
    else:
        return chr_null